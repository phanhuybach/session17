let result1 = Math.abs(-5.5);
let result2 = Math.abs(10);
console.log(result1);
console.log(result2);
//Hàm math.abs là hàm lấy giá trị tuyệt đối của một số nguyên

let result3 = Math.ceil(4.4);
let result4 = Math.ceil(5.8);
console.log(result3);
console.log(result4);
// hàm math.ceil là hàm làm tròn lên só nguyên gần nó nhất (ví dụ : 5.7-->6, 4.4-->5)

let result5 = Math.floor(5.7);
let result6 = Math.floor(6.1);
console.log(result5);
console.log(result6);
//Hàm math.floor là hàm làm tròn xuống số nguyên gần nó nhất

let result7 = Math.round(10.1);
let result8 = Math.round(20.6);
console.log(result7);
console.log(result8);
// Hàm math.round là hàm làm tròn đến số nguyên gần nó nhất nếu phần phân số  của đối số lớn hơn 0.5 thì đối số đó làm tròn đến số nguyên có giá trị tuyệt đối cao hơn , nếu còn nhở hơn 0.5 thì ngược lại

